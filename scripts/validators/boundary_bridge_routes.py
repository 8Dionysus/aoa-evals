"""Boundary-bridge route, sibling-ref, and handoff contracts."""

from __future__ import annotations

from pathlib import Path

from validators.boundary_bridge_common import (
    BOUNDARY_BRIDGE_COMPATIBILITY_MAP_DOC_NAME,
    BOUNDARY_BRIDGE_COMPATIBILITY_MAP_DOC_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_COMPATIBILITY_PART_README_NAME,
    BOUNDARY_BRIDGE_COMPATIBILITY_PART_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_DECISION_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_LATEST_SIBLING_CANARY_PART_README_NAME,
    BOUNDARY_BRIDGE_LATEST_SIBLING_CANARY_PART_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_LEGACY_DISTILLATION_LOG_NAME,
    BOUNDARY_BRIDGE_LEGACY_DISTILLATION_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_LEGACY_INDEX_NAME,
    BOUNDARY_BRIDGE_LEGACY_INDEX_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_LEGACY_RAW_README_NAME,
    BOUNDARY_BRIDGE_LEGACY_RAW_README_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_MECHANIC_AGENTS_NAME,
    BOUNDARY_BRIDGE_MECHANIC_AGENTS_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_MECHANIC_DECISION_NAME,
    BOUNDARY_BRIDGE_MECHANIC_PARTS_NAME,
    BOUNDARY_BRIDGE_MECHANIC_PARTS_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_MECHANIC_PROVENANCE_NAME,
    BOUNDARY_BRIDGE_MECHANIC_PROVENANCE_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_MECHANIC_README_NAME,
    BOUNDARY_BRIDGE_MECHANIC_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_ORCHESTRATOR_PART_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_ORCHESTRATOR_PROOF_ANCHORS_PART_README_NAME,
    BOUNDARY_BRIDGE_PARTS_README_NAME,
    BOUNDARY_BRIDGE_PARTS_README_REQUIRED_TOKENS,
    BOUNDARY_BRIDGE_PART_CONTRACT_GUARD_DECISION_NAME,
    BOUNDARY_BRIDGE_PART_CONTRACT_GUARD_DECISION_REQUIRED_TOKENS,
)
from validators.common import ValidationIssue
from validators.root_route_tokens import DECISION_RECORDS_README_NAME, require_tokens


def validate_boundary_bridge_route_surfaces(repo_root: Path) -> list[ValidationIssue]:
    issues: list[ValidationIssue] = []
    for path_name, tokens in (
        (BOUNDARY_BRIDGE_COMPATIBILITY_MAP_DOC_NAME, BOUNDARY_BRIDGE_COMPATIBILITY_MAP_DOC_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_MECHANIC_README_NAME, BOUNDARY_BRIDGE_MECHANIC_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_MECHANIC_AGENTS_NAME, BOUNDARY_BRIDGE_MECHANIC_AGENTS_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_MECHANIC_PARTS_NAME, BOUNDARY_BRIDGE_MECHANIC_PARTS_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_MECHANIC_PROVENANCE_NAME, BOUNDARY_BRIDGE_MECHANIC_PROVENANCE_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_LEGACY_INDEX_NAME, BOUNDARY_BRIDGE_LEGACY_INDEX_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_LEGACY_DISTILLATION_LOG_NAME, BOUNDARY_BRIDGE_LEGACY_DISTILLATION_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_LEGACY_RAW_README_NAME, BOUNDARY_BRIDGE_LEGACY_RAW_README_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_PARTS_README_NAME, BOUNDARY_BRIDGE_PARTS_README_REQUIRED_TOKENS),
        (BOUNDARY_BRIDGE_COMPATIBILITY_PART_README_NAME, BOUNDARY_BRIDGE_COMPATIBILITY_PART_REQUIRED_TOKENS),
        (
            BOUNDARY_BRIDGE_LATEST_SIBLING_CANARY_PART_README_NAME,
            BOUNDARY_BRIDGE_LATEST_SIBLING_CANARY_PART_REQUIRED_TOKENS,
        ),
        (
            BOUNDARY_BRIDGE_ORCHESTRATOR_PROOF_ANCHORS_PART_README_NAME,
            BOUNDARY_BRIDGE_ORCHESTRATOR_PART_REQUIRED_TOKENS,
        ),
        (
            BOUNDARY_BRIDGE_PART_CONTRACT_GUARD_DECISION_NAME,
            BOUNDARY_BRIDGE_PART_CONTRACT_GUARD_DECISION_REQUIRED_TOKENS,
        ),
        (
            DECISION_RECORDS_README_NAME,
            (
                BOUNDARY_BRIDGE_PART_CONTRACT_GUARD_DECISION_NAME,
                "Boundary Bridge Part Contract Guard",
            ),
        ),
        (BOUNDARY_BRIDGE_MECHANIC_DECISION_NAME, BOUNDARY_BRIDGE_DECISION_REQUIRED_TOKENS),
    ):
        require_tokens(repo_root=repo_root, path_name=path_name, tokens=tokens, issues=issues)
    return issues


__all__ = ("validate_boundary_bridge_route_surfaces",)
