def _read_text(repo_root: Path, relative_path: Path, issues: list[tuple[str, str]]) -> str:
    path = repo_root / relative_path
    if not path.is_file():
        issues.append((relative_path.as_posix(), "generated parity surface is missing"))
        return ""
    return path.read_text(encoding="utf-8")


def validate_generated_parity(repo_root: Path) -> list[tuple[str, str]]:
    issues: list[tuple[str, str]] = []

    generated_readme = _read_text(repo_root, GENERATED_README, issues)
    generated_agents = _read_text(repo_root, GENERATED_AGENTS, issues)
    docs_readme = _read_text(repo_root, DOCS_README, issues)

    for reader_path, builder in ROOT_READERS.items():
        if not (repo_root / reader_path).is_file():
            issues.append((reader_path.as_posix(), "generated root reader is missing"))
        if generated_readme:
            if reader_path.as_posix() not in generated_readme:
                issues.append(
                    (GENERATED_README.as_posix(), f"generated reader index must mention {reader_path.as_posix()}")
                )
            if builder not in generated_readme:
                issues.append(
                    (GENERATED_README.as_posix(), f"generated reader index must route {reader_path.as_posix()} to {builder}")
                )

    for command in GENERATOR_CHECK_COMMANDS:
        if generated_agents and command not in generated_agents:
            issues.append(
                (GENERATED_AGENTS.as_posix(), f"generated route card must expose check command {command!r}")
            )

    for generated_dir in PART_LOCAL_GENERATED_SURFACES:
        if not (repo_root / generated_dir).is_dir():
            issues.append((generated_dir.as_posix(), "part-local generated reader district is missing"))

    for index_path in DECISION_INDEXES:
        text = _read_text(repo_root, index_path, issues)
        if text and GENERATED_NOTICE not in text:
            issues.append((index_path.as_posix(), "generated decision index must carry generator notice"))

    for token in ("generated/eval_report_index.min.json", "generated/comparison_spine.json"):
        if docs_readme and token not in docs_readme:
            issues.append((DOCS_README.as_posix(), f"docs route chooser must link generated reader {token}"))

    return issues
